    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        if (varNombreEntrenador.getText().isEmpty() || varNombreEquipo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llenar todos los campos!");
        } else {

            // se llama la clase de equipos
            AgregarEquipo hola = new AgregarEquipo();

            equipo.setNombreEquipo(varNombreEquipo.getText());
            equipo.setNombreEntrenador(varNombreEntrenador.getText());
            String Nombre = equipo.getNombreEquipo();
            boolean uwu = archivo.agregarEquipo(Nombre);
            System.out.println(""+uwu);
           
            
            // tener un numero al azar
            int min = 1000;
            int max = 5000;
            equipo.setNumeroRandom(hola.getNumeroRandom(min, max));
            
            
            //mandar los datos al archivo
            String fila = equipo.getNumeroRandom() + ";" + equipo.getNombreEquipo() + ";" + equipo.getNombreEntrenador() + "\n";

            //le mandamos a la clase archivo la fila
            archivo.escribir(fila);

            // para saber como esta el archivo
            System.out.print(fila);

            // vacia los jtextfield
            varNombreEquipo.setText("");
            varNombreEntrenador.setText("");

            // muestra el mensaje y el codigo del equipo
            JOptionPane.showMessageDialog(null, """
                                                Equipo Registrado Correctamente
                                                Codigo del equipo: 
                                                """ + equipo.getNumeroRandom());
        
              
            
        }
    }//GEN-LAST:event_BotonGuardarActionPerformed

    // metodo para crear un numero al azar
    private int getNumeroRandom(int min, int max) {
        return ThreadLocalRandom.current().nextInt(min, max);
    }

